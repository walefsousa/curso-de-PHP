<?php


//------- Comparação --------

if(5 == 5){
    echo "Os valores são iguais!";
}

//------- Operador idêntico -------

if(5 === 5){
    echo "Os valores são iguais!";
}

//----- Diferente ------
if(5 != 3){
    echo "Os valores são diferentes!";
}

//------- Operador não idêntico -------

if(5 !== "5"){
    echo "Os valores são diferentes";
}